// Parse the downloaded game-icons.net SVGs (CC BY 3.0) in art-src/creatures into
// a TS data module. Strips the 512×512 background rect and keeps the foreground
// path(s). Re-run after adding/replacing source art:  node scripts/build-creature-art.mjs
import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const srcDir = join(here, "../art-src/creatures");

// id -> "author/icon" provenance (for the comment header + ATTRIBUTION.md).
const SOURCE = {
  reaper: "lorc/grim-reaper", "dark-angel": "lorc/angel-outfit", demon: "lorc/imp",
  knight: "lorc/visored-helm", valkyrie: "lorc/winged-sword", gargoyle: "delapouite/gargoyle",
  wraith: "lorc/spectre", sorcerer: "lorc/wizard-staff", vampire: "delapouite/vampire-dracula",
  berserker: "delapouite/barbarian",
};

const BG = "M0 0h512v512H0z";
const out = {};
for (const file of readdirSync(srcDir)) {
  if (!file.endsWith(".svg")) continue;
  const id = file.replace(/\.svg$/, "");
  const svg = readFileSync(join(srcDir, file), "utf8");
  out[id] = [...svg.matchAll(/<path[^>]*\bd="([^"]+)"/g)]
    .map((m) => m[1])
    .filter((d) => d.trim() !== BG);
}

const lines = [
  "// AUTO-GENERATED by scripts/build-creature-art.mjs — do not edit by hand.",
  "// Art from game-icons.net, licensed CC BY 3.0 (see ATTRIBUTION.md). Source:",
  ...Object.entries(SOURCE).map(([id, s]) => `//   ${id} ← ${s}`),
  "// Each entry is the foreground path(s) of a 512×512 icon (bg rect stripped).",
  "",
  "export const CREATURE_ART: Record<string, string[]> = {",
  ...Object.entries(out).map(
    ([id, ds]) => `  ${JSON.stringify(id)}: [${ds.map((d) => JSON.stringify(d)).join(", ")}],`,
  ),
  "};",
  "",
];
writeFileSync(join(here, "../src/data/creatureArt.ts"), lines.join("\n"));
console.log("wrote src/data/creatureArt.ts with", Object.keys(out).length, "creatures");
